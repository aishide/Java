public class People_count { 
    public static void main (String args[]) {
        System.out.println("This is a demo basic program which counts the number of people in the room.");
        int people = 0;
        people++;
        people++;
        System.out.println("Current people inside the room : " + people);
        people++;
        people--;
        people--;
        System.out.println("Current people inside the room : " + people);
        people++;
        people++;
        System.out.println("Current people inside the room : " + people);

    }
}